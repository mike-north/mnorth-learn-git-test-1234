
def add(a, b):
    """
    Add some numbers together

    ```py
    add(2, 3) # 5
    ```
    """
    return a + b

def sub(a, b):
    """
    Subtract some numbers

    ```py
    sub(8, 3) # 5
    ```
    """
    return a - b


def mult(a, b):
    """
    Multiply some numbers

    ```py
    mult(8, 3) # 24
    ```
    """
    return a * b